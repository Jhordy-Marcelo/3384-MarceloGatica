using System;

class RepetitivaWhile1
{
    static void Main()
    {
        Console.WriteLine("=== Contar del 1 al 5 ===");

        int i = 1;

        // Bucle while
        while (i <= 5)
        {
            Console.WriteLine(i);
            i++; // incremento del contador
        }
    }
}
